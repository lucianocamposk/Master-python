"""
EJERCICIO 4. pedir dos numeros al usuario y hacer
todas las operaciones basicas de una calculadora y mostrarlo por pantalla
"""
numero1=int(input("Introduce un numero: "))
numero2=int(input("Introduce otro numero: "))

print(f"{numero1} + {numero2} = {numero1+numero2}")
print(f"{numero1} - {numero2} = {numero1-numero2}")
print(f"{numero1} * {numero2} = {numero1*numero2}")
print(f"{numero1} / {numero2} = {numero1/numero2}")
